//Kotlin Program to Multiply two Floating Point Numbers and
//Program to Find ASCII value of a character
fun main()
{
     var numb1 : Float = 45.78f
     var numb2 : Float = 14.78f
     println("multiplication of two digits:${numb1*numb2}")

     var char : Char = 'k'
     println("ASCII value of $char is : ${char.hashCode()}")

}